    public static String getBox(String mailName) {
        StringBuilder addr = new StringBuilder("https://mailtemp.top/api/v1/mailbox/");
        addr.append(mailName);
        String body = null;
        int status = 0;
        //status < 10更改数值，相当于更改收件服务器等待时间
        while (status != -1 && status < 60){
            status ++;
            try {
                body = sendGet(addr.toString(), null);
                if(body != null && body != "" && body != "[]"){
                    status = -1;
                }
                Thread.sleep(1000);
            } catch (Exception e) {
                e.printStackTrace();
                System.out.println("获取消息失败," + e.getMessage());
            }
        }
        StringBuilder url = new StringBuilder("https://mailtemp.top/mailbox/");
        List<ResponseMessage> messages = JSONArray.parseArray(body, ResponseMessage.class);
        ResponseMessage message = messages.get(0);
        url.append(mailName).append("/").append(message.getId());
        String html = sendGet(url.toString(), null);
        String div = "<div role=\"tabpanel\" class=\"tab-pane message-body\" id=\"body-text\">";
        return html.substring(html.indexOf(div) + div.length(), html.lastIndexOf("</div></div>"));
    }

    public static String sendGet(String url, String param) {
        String result = "";
        BufferedReader in = null;
        try {
            CookieManager cookieManager = new CookieManager();
            CookieHandler.setDefault(cookieManager);
            String urlNameString = url + "?" + param;
            URL realUrl = new URL(urlNameString);
            // 打开和URL之间的连接
            URLConnection connection = realUrl.openConnection();
            // 设置通用的请求属性
            connection.setRequestProperty("accept", "*/*");
            connection.setRequestProperty("connection", "Keep-Alive");
            connection.setRequestProperty("user-agent", "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/92.0.4515.159 Safari/537.36");
            // 建立实际的连接
            connection.connect();
            // 获取所有响应头字段
            Map<String, List<String>> map = connection.getHeaderFields();
            // 遍历所有的响应头字段
            for (String key : map.keySet()) {
                System.out.println(key + "--->" + map.get(key));
            }
            // 定义 BufferedReader输入流来读取URL的响应
            in = new BufferedReader(new InputStreamReader(connection.getInputStream()));
            String line;
            while ((line = in.readLine()) != null) {
                result += line;
            }
            List<HttpCookie> cookies = cookieManager.getCookieStore().getCookies();
            for(HttpCookie cookie : cookies){
                System.out.println(cookie.getDomain());
                System.out.println(cookie);
            }
        } catch (Exception e) {
            System.out.println("发送GET请求出现异常！" + e);
            e.printStackTrace();
        }
        // 使用finally块来关闭输入流
        finally {
            try {
                if (in != null) {
                    in.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }
        return result;
    }

